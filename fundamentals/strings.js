const hello = "Hello World";
console.log(hello.charAt(2)); // O método charAt() retorna o caractere especificado a partir de uma string.
console.log(hello.charCodeAt(3)); // O charCodeAt()método retorna o Unicode do caractere em um índice especificado (posição) em uma string.
console.log(hello.indexOf("l")); // O indexOf()método retorna a posição da primeira ocorrência de um valor em uma string.
console.log(hello.substring(3)); // O substring()método extrai caracteres, entre dois índices (posições), de uma string e retorna a substring.
console.log(hello.substring(3, 5));
console.log(hello.replace("H", "h")); // O replace()método retorna uma nova string com o(s) valor(es) substituído(s).
console.log("Wanderley, Lucas, Felipe".split(",")); // O split()método divide uma string em uma matriz de substrings.
